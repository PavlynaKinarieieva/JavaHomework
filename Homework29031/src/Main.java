import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        System.out.println("Введите название блюда, которое вы хотите приготовить:");
        String dish = scanner.nextLine();

        switch (dish.toLowerCase()) {
            case "борщ":
                System.out.println("Рецепт борща: нарезать мясо и овощи, сварить в кипящей воде, добавить свеклу и томатную пасту, посолить и поперчить по вкусу.");
                break;
            case "плов":
                System.out.println("Рецепт плова: обжарить мясо, лук и морковь, добавить рис, залив водой, довести до кипения, посолить и поперчить, тушить на медленном огне.");
                break;
            case "лазанья":
                System.out.println("Рецепт лазаньи: приготовить соус из мяса, томатов и специй, затем сложить с соусом слои лазаньи и сыра, запекать в духовке.");
                break;
            case "гуляш":
                System.out.println("Рецепт гуляша: обжарить мясо и лук, добавить морковь и специи, залить водой, тушить на медленном огне до готовности.");
                break;
            case "рисотто":
                System.out.println("Рецепт рисотто: обжарить рис с луком, добавить бульон, постепенно добавлять сыр и сливки, перемешивать до готовности.");
                break;
            case "оливье":
                System.out.println("Рецепт оливье: нарезать вареные овощи, яйца и мясо, смешать с майонезом, посолить и поперчить.");
                break;
            case "салат цезарь":
                System.out.println("Рецепт салата цезарь: нарезать салат, добавить жареные куриные кусочки, сыр, гренки, заправить соусом из майонеза, чеснока и лимонного сока.");
                break;
            case "пицца":
                System.out.println("Рецепт пиццы: замесить тесто, добавить соус, сыр и любые ингредиенты на ваш выбор, запекать в духовке.");
                break;
            default:
                System.out.println("К сожалению, рецепта на такое блюдо у нас нет.");
        }
    }
}
